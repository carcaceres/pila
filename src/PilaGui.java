    public void mostrarPila(){
        int i;
        mostrarArea.setText("");
        String[] pil=pila.pila();
        for(i=0;i<pil.length;i++){
            if(pil[i]!=null){
            mostrarArea.append("|"+pil[i]+"|\n");
            }
            else{
                mostrarArea.append("|  |\n");
            }
        }
    }
    
    
