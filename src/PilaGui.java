    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(elemText.getText().length()>0){
        pila.llenarPila(elemText.getText());
        elemText.setText("");
        elemText.requestFocus();
          JOptionPane.showMessageDialog(rootPane, 
                  "Se agrego elemento a la pila "+elemText.getText());
          mostrarPila();
        }
        else{
         JOptionPane.showMessageDialog(rootPane, 
                  "Campo Vacio!! "+elemText.getText());
         elemText.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(tamText.getText().length()>0){
        pila.tamPila(Integer.valueOf(tamText.getText()));
        JOptionPane.showMessageDialog(rootPane, "Pila Creada de tamaño "+
                tamText.getText());
        elemText.requestFocus();
        }
        else{
         JOptionPane.showMessageDialog(rootPane, "Campo Vacio!! "); 
         tamText.requestFocus();
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

